#!/bin/bash
echo "================================================"
echo "          📊 节点网络流量消耗统计看板           "
echo "================================================"

echo ""
echo "▶ 【VPS 整体出入站总流量】 (ens5 物理网卡)"
echo "------------------------------------------------"
vnstat -i ens5 2>/dev/null || echo "数据收集不足，请等待几分钟。"

echo ""
echo "▶ 【OpenCode 代理项目专属流量】 (主隧道: tun0)"
echo "------------------------------------------------"
vnstat -i tun0 2>/dev/null || echo "主隧道数据收集不足或暂无流量。"

echo ""
echo "▶ 【OpenCode 代理项目专属流量】 (备用无缝隧道: tun1)"
echo "------------------------------------------------"
vnstat -i tun1 2>/dev/null || echo "备用无缝隧道数据收集不足或暂无流量。"

echo ""
echo "💡 提示: 由于您启用了双活轮换，真实消耗总流量为主副隧道之和。"
